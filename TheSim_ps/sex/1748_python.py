import sys
input = lambda : sys.stdin.readline().rstrip()

N=int(input())
sum=0

if N<10: #1의 자리
    sum+=N
elif N<100: #10의 자리
    sum+=9
    sum+=(N-10+1)*2
elif N<1000: #100의 자리
    sum+=189
    sum+=(N-100+1)*3
elif N<10000: #1000의 자리
    sum+=(189+2700)
    sum+=(N-1000+1)*4
elif N<100000: #10000의 자리
    sum+=(189+2700+36000)
    sum+=(N-10000+1)*5
elif N<1000000: #100000의 자리
    sum+=(189+2700+36000+450000)
    sum+=(N-100000+1)*6
elif N<10000000: #1,000,000의 자리
    sum+=(189+(900*3)+(9000*4)+(90000*5)+(900000*6))
    sum+=(N-1000000+1)*7
elif N<100000000: #10,000,000의 자리
    sum+=(189+(900*3)+(9000*4)+(90000*5)+(900000*6)+(9000000*7))
    sum+=(N-10000000+1)*8
elif N<1000000000: #100,000,000의 자리
    sum+=(189+(900*3)+(9000*4)+(90000*5)+(900000*6)+(9000000*7)+(90000000*8))
    sum+=(N-100000000+1)*9

print(sum)

